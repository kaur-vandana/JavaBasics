package com.kv.javaCourse.basic;

public class ForLoop {

    public static void main(String[] args) {
//        for(initialization; termination; increment)
        for(int i=0; i<5; i++) {
            System.out.println(i);
        }

        for(int j=100; j>90; j--) {
            System.out.println(j);
        }
    }
}
